#inicio da questão 01
value_a = int(input("Digite um valor: "))
value_b = int(input("Digite outro valor: "))

print(f"{value_a} + {value_b} → {value_a + value_b}")
print(f"{value_a} - {value_b} → {value_a - value_b}")
print(f"{value_a} * {value_b} → {value_a * value_b}")
print(f"{value_a} / {value_b} → {value_a / value_b}")

#fim da questão 01

